public class AssignmentOperator {

    public static void main(String[] args){

        int m1,m2,m3,m4;
        m1=m2=m3=m4=10;
        System.out.println(m1 + " " + m2 + " " + m3 + " " + m4);

        int a=20;
        a+=5;
        System.out.println(a);

        int b=10;
        b-=5;
        System.out.println(b);

        int c=20;
        c/=5;
        System.out.println(c);

        int d=5;
        d*=5;
        System.out.println(d);


    }
    
}
