
public class While_Loop_DoWhile_Loop {

    public static void main(String[] args){

        int i=0;
        while(i<5){
            System.out.println("*");
            i++;
        }

        int j=0;
        do{
            System.out.println("code executed");
            j++;
        }while(j<4);

    }
    
}
