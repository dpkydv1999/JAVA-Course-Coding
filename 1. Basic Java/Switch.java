public class Switch {

    public static void main(String[] args){
        int num=1899;

        switch(num){
            case 1300: System.out.println("case1");
            break;
            case 200: System.out.println("case2");
            break;
            case 100: System.out.println("case3");
            break;
            default: System.out.println("default");
        }
    }
    
}
